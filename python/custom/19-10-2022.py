# Matematikada funktsiya belgisi(x) (sonning belgisi) quyidagicha aniqlanadi:
# belgisi (x) = 1, agar x > 0 bo'lsa,
# belgisi (x) = -1, agar x < 0 bo'lsa,
# belgisi (x) = 0, agar x = 0 bo'lsa.
# Berilgan x soni uchun (x) belgisi qiymatini chop eting.
x=int(input('x='))
if x>0:
    print('1')
elif x<0:
    print('-1')
else:
    print('0')





